package com.isooou.message.service;

import java.util.Date;
import java.util.Map;

import javax.servlet.http.HttpServletRequest;

import com.isooou.message.resp.TextMessage;
import com.isooou.message.util.MessageUtil;

public class CoreService {
	/*
	 * 处理微信发来的请求
	 * */
	public static String processRequest(HttpServletRequest request){
		//xml格式的消息数据
		String respXml = null;
		//默认返回的文本消息内容
		String respContent = "未知的消息类型！";
		try{
			//调用parseXml方法解析请求消息
			Map<String,String> requestMap = MessageUtil.parseXml(request);
			//发送方账号
			String fromUserName = requestMap.get("FromUserName");
			//开发者微信号
			String toUserName = requestMap.get("ToUserName");
			//消息类型
			String msgType = requestMap.get("MsgType");
			
			//回复文本消息
			TextMessage textMessage = new TextMessage();
			textMessage.setToUserName(fromUserName);
			textMessage.setFromUserName(toUserName);
			textMessage.setCreateTime(new Date().getTime());
			textMessage.setMsgType(MessageUtil.RESP_MESSAGE_TYPE_TEXT);
			
			//文本消息
			if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_TEXT)){
				respContent = "您发送的是文本消息！";
			}
			//图片消息
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_IMAGE)){
				respContent = "您发送的是图片消息！";
			}
			//语音消息
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_VOICE)){
				respContent = "您发送的是语音消息！";
			}
			//视频消息
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_VIDEO)){
				respContent = "您发送的是视频消息！";
			}
			//地理位置消息
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_LOCATION)){
				respContent = "您发送的是地理位置消息！";
			}
			//链接消息
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_LINK)){
				respContent = "您发送的是链接消息！";
			}
			//事件推送
			else if(msgType.equals(MessageUtil.REQ_MESSAGE_TYPE_EVENT)){
				//事件类型
				String eventType = requestMap.get("Event");
				//关注事件
				if(eventType.equals(MessageUtil.EVENT_TYPE_SUBSCRIBE)){
					respContent = "谢谢您的关注！";
				}
				//取消关注
				else if(eventType.equals(MessageUtil.EVENT_TYPE_NUSUBSCRIBE)){
					//TODO 取消关注后，用户不会再收到公众账号发送的消息，因此不需要回复
				}
				//扫描带参数二维码
				else if(eventType.equals(MessageUtil.EVENT_TYPE_SCAN)){
					//TODO 处理扫描带参数二维码事件
				}
				//上报地理位置
				else if(eventType.equals(MessageUtil.EVENT_TYPE_LOCATION)){
					//TODO 处理上报地理位置
				}
				//自定义菜单
				else if(eventType.equals(MessageUtil.EVENT_TYPE_CLICK)){
					respContent = "错了；";
					//事件类型
					//String eventType_ = requestMap.get("Event");
					//自定义菜单点击事(CLICK)件
				//	if(eventType_.equals("CLICK")){
						//事件KEY值，与创建菜单时的key值对应
						String eventKey = requestMap.get("EventKey");
						System.out.println(eventKey);
						//根据key值判断用户点击的按钮
						if(eventKey.equals("V1001_TODAY_MUSIC")){
							respContent = "用户点击了“今日歌曲”按钮";
						}else if(eventKey.equals("V1001_HELLO_WORD")){
							respContent = "用户点击了“hello word”按钮";
						}else if(eventKey.equals("V1001_GOOD")){
							respContent = "用户点击了“赞一下我们”按钮";
						}
					//}
				}
			}
			//设置文本消息内容
			textMessage.setContent(respContent);
			//将文本消息对象转换成xml
			respXml = MessageUtil.messageToXml(textMessage);
		}catch(Exception e){
			e.printStackTrace();
		}
		
		return respXml;
	}
}
